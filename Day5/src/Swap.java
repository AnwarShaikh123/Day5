
public class Swap {

	public static void main(String[] args) {
		int a=3,b=6,c;
		System.out.println("--------Before Swap----------");
		
		System.out.println("The value of a is: " + a);
		System.out.println("The value of b is: " + b);
		c=a;
		a=b;
		b=c;
		System.out.println("--------After Swap--------");
		
System.out.println("The value of a is: " + a);
System.out.println("The value of b is: " + b);
	}

}
